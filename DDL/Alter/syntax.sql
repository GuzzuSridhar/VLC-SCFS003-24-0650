/*
The ALTER TABLE statement in SQL is used to modify an existing table's structure. 
This can include adding, deleting, or modifying columns, adding constraints, or changing the table's name.
Syntax
ALTER TABLE table_name
    action;
    
    Common Actions:
    ADD: Adds new columns or constraints.
	DROP: Removes columns or constraints.
	MODIFY: Changes the data type or constraints of existing columns.
	RENAME: Changes column names or table names.
	ADD CONSTRAINT: Adds new constraints to the table.
	DROP CONSTRAINT: Removes existing constraints.
*/